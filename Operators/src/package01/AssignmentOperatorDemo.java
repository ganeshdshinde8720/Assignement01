package package01;

public class AssignmentOperatorDemo {

	public static void main(String[] args) {
		int i=42;
		
		i -= 4;  // 38
		System.out.println(i);
		
		i += 10; // 48
		System.out.println(i);
		
		i *=6;   // 288
		System.out.println(i);
		
		i /= 7;  // 41.14
		System.out.println(i);

	}

}
